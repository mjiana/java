package chapter04;

public class If3 {

	public static void main(String[] args) {
		// 조건문 if-else
		//블록 구분을 잘 하지않으면 헷갈릴 수 있다
		int k = 100;
		if(k == 100)
			System.out.println("k는 100입니다.");
		else
			System.out.println("-----------");
		//아래는 조건문과 상관없는 처리식
		System.out.println("k는 100이 아닙니다.");
	}

}
